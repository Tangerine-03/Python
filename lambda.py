# #Square of a number using lambda function
square=lambda x: x*x
n=int(input("Enter a number: "))
print("The square of ",n, "is", square(n))

# #Sum of two numbers using lambda function
sum=lambda a,b: a+b
n1=int(input("Enter first number: "))
n2=int(input("Enter second number: "))
print("The sum of ",n1, "and", n2, "is", sum(n1,n2))


